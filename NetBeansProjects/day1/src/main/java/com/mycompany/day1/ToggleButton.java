    public void calcular(){
        double precio = 0;

        try{
            precio = Double.parseDouble(jTextPanelUser.getText()); 
            
            if(jToggleButtonInstallation.isSelected()){
                precio += 60;
            }
            if(jToggleButtonTraining.isSelected()){
                precio += 100;
            }
            if(jToggleButtonPower.isSelected()){
                precio += 250;
            }
            
            String texto = String.valueOf(precio);
            
            jLabelFinalPrice.setText(texto + " â‚¬");
            
        }catch(Exception e){
            jLabelFinalPrice.setText("Error");
        }
    }
    
